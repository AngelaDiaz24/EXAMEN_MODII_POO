    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        String codigo = txtCodigo.getText();
        String nombre = txtNombre.getText();
        String direccion = txtDireccion.getText();
        String talla = txtTalla.getText();
        String correo = txtCorreo.getText();
        try {
            Date fecha = formato.parse(txtFecha.getText());
            Docente a = new Docente(codigo, nombre, direccion, fecha, talla, correo, null, null);
            docDAO.agregar(a);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Fecha invÃ¡lida. Use el formato dd/MM/yyyy");
        }
        txtCodigo.setText("");
        txtNombre.setText("");
        txtDireccion.setText("");
        txtFecha.setText("");
        txtTalla.setText("");
        txtCorreo.setText("");
    }//GEN-LAST:event_btnAgregarActionPerformed

